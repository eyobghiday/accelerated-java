package lectures.one;

public class ExampleOne {

	public static void main(String[] args) {
		
		int x = 42;
		int y = 64;
		
		if (x < y) {
			System.out.println("X is less than Y");
		} else if (x == y) {
			System.out.println("X is equal to Y");
		} else {
			System.out.println("X is greater than Y");
		}
		
	}

}
