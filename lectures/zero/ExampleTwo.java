package lectures.zero;

public class ExampleTwo {

	public static void main(String[] args) {
		int x = 2;
		int y = 2 + 1;
		
		System.out.println(x + y);
		System.out.println(y - x);
		System.out.println(x * y);
		System.out.println(y / x);
		System.out.println(y % x);
		
		System.out.println(x < y);
		System.out.println(x <= y);
		System.out.println(x > y);
		System.out.println(x >= y);
		System.out.println(x == y);
		System.out.println(x != y);
	}

}
